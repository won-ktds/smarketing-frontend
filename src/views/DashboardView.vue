const animateNumber = (targetValue, index, duration = 2000) => {
  const startValue = 0
  const startTime = Date.now()

  const updateValue = () => {
    const elapsed = Date.now() - startTime
    const progress = Math.min(elapsed / duration, 1)

    // easeOutCubic Ïï†ÎãàÎ©îÏù¥ÏÖò
    const easedProgress = 1 - Math.pow(1 - progress, 3)
    const currentValue = Math.floor(startValue + (targetValue - startValue) * easedProgress)

    // Ïà´Ïûê ÌòïÏãùÏóê Îî∞Îùº ÌëúÏãú
    if (typeof targetValue === 'number') {
      if (index === 0 || index === 1) {
        // Îß§Ï∂ú - ÏõêÌôî ÌëúÏãú
        animatedValues.value[index] = `‚Ç©${currentValue.toLocaleString()}`
      } else {
        // ÏùºÎ∞ò Ïà´Ïûê (Ï°∞ÌöåÏàò Îì±)
        animatedValues.value[index] = currentValue.toLocaleString()
      }
    }

    if (progress < 1) {
      requestAnimationFrame(updateValue)
    }
  }

  updateValue()
}

